    public void actualizarListaQuartos() {
        DefaultListModel<Quarto> model = new DefaultListModel<>();
        for (Quarto quarto : DadosAplicacao.getInstance().getQuartos()) {
            model.addElement(quarto);
        }
        jList1.setModel(model);
    }

